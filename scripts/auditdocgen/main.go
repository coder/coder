package main

import (
	"bytes"
	"flag"
	"fmt"
	"log"
	"os"
	"strconv"
	"strings"

	"golang.org/x/xerrors"

	"github.com/coder/coder/v2/coderd/util/maps"
	"github.com/coder/coder/v2/enterprise/audit"
	"github.com/coder/coder/v2/scripts/atomicwrite"
)

var (
	auditDocFile  string
	dryRun        bool
	resourcesFlag string
	sentinelPath  string
)

// resourceNameRenames maps internal Go struct names to their user-facing
// labels. The internal names come from the database package struct names;
// some differ from what we display in the docs for clarity.
var resourceNameRenames = map[string]string{
	"AuditableGroup": "Group",
	"TaskTable":      "Task",
}

/*
*
AuditableResourcesMap is derived from audit.AuditableResources
and has the following structure:

	{
		friendlyResourceName: {
			fieldName1: isTracked,
			fieldName2: isTracked,
			...
		},
		...
	}
*/
type AuditableResourcesMap map[string]map[string]bool

func main() {
	flag.StringVar(&auditDocFile, "audit-doc-file", "docs/admin/security/audit-logs.md", "Path to audit log doc file")
	flag.BoolVar(&dryRun, "dry-run", false, "Dry run")
	flag.StringVar(&resourcesFlag, "resources", "", "Comma-separated allowlist of resource names to include (empty = all)")
	flag.StringVar(&sentinelPath, "sentinel-path", "docs/admin/security/audit-logs.md", "Doc path to embed in generated sentinel comments")
	flag.Parse()

	generatorPrefix := fmt.Sprintf("<!-- Code generated by 'make %s'. DO NOT EDIT -->", sentinelPath)
	generatorSuffix := fmt.Sprintf("<!-- End generated by 'make %s'. -->", sentinelPath)

	allowlist := parseResourceAllowlist(resourcesFlag)
	auditableResourcesMap := readAuditableResources(allowlist)

	doc, err := readAuditDoc()
	if err != nil {
		log.Fatal("can't read audit doc: ", err)
	}

	doc, err = updateAuditDoc(doc, auditableResourcesMap, []byte(generatorPrefix), []byte(generatorSuffix))
	if err != nil {
		log.Fatal("can't update audit doc: ", err)
	}

	if dryRun {
		log.Println(string(doc))
		return
	}

	err = writeAuditDoc(doc)
	if err != nil {
		log.Fatal("can't write updated audit doc: ", err)
	}
}

// parseResourceAllowlist parses a comma-separated list of resource names into
// a set. Returns nil if the input is empty, meaning all resources are included.
func parseResourceAllowlist(s string) map[string]struct{} {
	if s == "" {
		return nil
	}
	allowlist := make(map[string]struct{})
	for _, name := range strings.Split(s, ",") {
		name = strings.TrimSpace(name)
		if name != "" {
			allowlist[name] = struct{}{}
		}
	}
	return allowlist
}

// Transforms audit.AuditableResources to AuditableResourcesMap,
// which uses friendlier language. If allowlist is non-nil, only resources
// whose (possibly renamed) name appears in the allowlist are included.
func readAuditableResources(allowlist map[string]struct{}) AuditableResourcesMap {
	auditableResourcesMap := make(AuditableResourcesMap)

	for resourceName, resourceFields := range audit.AuditableResources {
		friendlyResourceName := strings.Split(resourceName, ".")[2]

		// Apply any renames so the map keys match AuditActionMap entries
		// and the user-facing labels in the documentation.
		if renamed, ok := resourceNameRenames[friendlyResourceName]; ok {
			friendlyResourceName = renamed
		}

		// Filter by allowlist when specified.
		if allowlist != nil {
			if _, ok := allowlist[friendlyResourceName]; !ok {
				continue
			}
		}

		fieldNameMap := make(map[string]bool)
		for fieldName, action := range resourceFields {
			fieldNameMap[fieldName] = action != audit.ActionIgnore
		}
		auditableResourcesMap[friendlyResourceName] = fieldNameMap
	}

	return auditableResourcesMap
}

func readAuditDoc() ([]byte, error) {
	doc, err := os.ReadFile(auditDocFile)
	if err != nil {
		return nil, err
	}

	return doc, nil
}

// updateAuditDoc writes a markdown table of audit log resources into doc,
// replacing content between generatorPrefix and generatorSuffix.
func updateAuditDoc(doc []byte, auditableResourcesMap AuditableResourcesMap, generatorPrefix, generatorSuffix []byte) ([]byte, error) {
	// We must sort the resources to ensure table ordering
	sortedResourceNames := maps.SortedKeys(auditableResourcesMap)

	i := bytes.Index(doc, generatorPrefix)
	if i < 0 {
		return nil, xerrors.New("generator prefix tag not found")
	}
	tableStartIndex := i + len(generatorPrefix) + 1

	j := bytes.Index(doc[tableStartIndex:], generatorSuffix)
	if j < 0 {
		return nil, xerrors.New("generator suffix tag not found")
	}
	tableEndIndex := tableStartIndex + j

	var buffer bytes.Buffer
	_, _ = buffer.Write(doc[:tableStartIndex])
	_ = buffer.WriteByte('\n')

	_, _ = buffer.WriteString("|<b>Resource<b>||\n")
	_, _ = buffer.WriteString("|--|-----------------||\n")

	for _, resourceName := range sortedResourceNames {
		// Create a string of audit actions for each resource
		var auditActions []string
		for _, action := range audit.AuditActionMap[resourceName] {
			auditActions = append(auditActions, string(action))
		}
		auditActionsString := strings.Join(auditActions, ", ")

		_, _ = buffer.WriteString("|" + resourceName + "<br><i>" + auditActionsString + "</i>|<table><thead><tr><th>Field</th><th>Tracked</th></tr></thead><tbody>" + "|")

		// We must sort the field names to ensure sub-table ordering
		sortedFieldNames := maps.SortedKeys(auditableResourcesMap[resourceName])

		for _, fieldName := range sortedFieldNames {
			isTracked := auditableResourcesMap[resourceName][fieldName]
			_, _ = buffer.WriteString("<tr><td>" + fieldName + "</td><td>" + strconv.FormatBool(isTracked) + "</td></tr>")
		}

		_, _ = buffer.WriteString("</tbody></table>\n")
	}

	_, _ = buffer.WriteString("\n")
	_, _ = buffer.Write(doc[tableEndIndex:])
	return buffer.Bytes(), nil
}

func writeAuditDoc(doc []byte) error {
	return atomicwrite.File(auditDocFile, doc)
}
